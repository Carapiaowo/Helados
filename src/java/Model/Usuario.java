package Model;

/**
 *
 * @author CarapiaOwO
 */
public class Usuario {
    private String nombre,direccion,correo,pass;
    private int id;

    public Usuario() {
    }

    public Usuario(String nombre, String direccion, String correo, String pass, int id) {
        this.nombre = nombre;
        this.direccion = direccion;
        this.correo = correo;
        this.pass = pass;
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
